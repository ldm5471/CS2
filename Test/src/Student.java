public class Student {
    String name;
    String major;
    double GPA;
    int academicYear;
    boolean livesOnCampus;

    public Student(String name, String major, double GPA, int academicYear, boolean livesOnCampus) {
        this.name = name;
        this.major = major;
        this.GPA = GPA;
        this.academicYear = academicYear;
        this.livesOnCampus = livesOnCampus;
    }


}
